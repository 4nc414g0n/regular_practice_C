// 5位运动员参加了10米台跳水比赛，有人让他们预测比赛结果：

// A选手说：B第二，我第三；

// B选手说：我第二，E第四；

// C选手说：我第一，D第二；

// D选手说：C最后，我第三；

// E选手说：我第四，A第一；

// 比赛结束后，每位选手都说对了一半，请编程确定比赛的名次。


#include <stdio.h>
int checkData(int *p)
{
	char tmp = 0;
 
	int i;
	for (i = 0; i < 5; i++)
	{
		tmp |= 1 << p[i]; 
        //tmp每次或上一位1，p[i]如果是1~5都有，则1<<1到1<<5都或上的结果将会是00111110，如果有并列，则一定会至少却其中一个1，
        //结果就不会是00111110，所以可以判断tmp最终的结果是不是这个数字来判断有没有重复。
	}
	return tmp == 0x3E;
}

void diveRank(int * p, int n)
{
	if(n >= 5) //此时的n是用来控制循环层数的。
	{
		if ((p[1] == 2) + (p[0] == 3) == 1 && //B第二，我第三
			(p[1] == 2) + (p[4] == 4) == 1 && //我第二，E第四
			(p[2] == 1) + (p[3] == 2) == 1 && //我第一，D第二
			(p[2] == 5) + (p[3] == 3) == 1 && //C最后，我第三
			(p[4] == 4) + (p[0] == 1) == 1 && //我第四，A第一
			checkData(p)) //查重
		{
			for (int i = 0; i < 5; i++)
			{
				printf("%d ", p[i]);
			}
			putchar('\n');
		}
		return;
	}
 
	for(p[n] = 1; p[n] <= 5; p[n]++)
	{
		diveRank(p, n + 1); //通过递归模拟多层循环，每进一次递归相当于进了一层新的循环。
	}
}
 
int main()
{
	int p[5];
 
	diveRank(p, 0);
 
	return 0;
}


